// ═══════════════════════════════════════
//  4. AUTHORSHIP FINGERPRINT
// ═══════════════════════════════════════

export interface GeminiAuthorshipResult {
  consistencyScore: number;      // 0–100
  dominantStyle: string;         // e.g. "Academic", "Informal", "Technical"
  voiceCharacteristics: string[];
  styleShifts: {
    location: string;
    description: string;
    severity: "high" | "medium" | "low";
    possibleExplanation: string;
  }[];
  writingPersonality: string;    // brief description of writing style
  authenticityVerdict: "consistent" | "mixed" | "suspicious";
  authenticityReasoning: string;
  recommendations: string[];
}

export async function geminiAuthorshipAnalysis(text: string): Promise<GeminiAuthorshipResult> {
  const truncated = text.slice(0, 5000);

  const prompt = `You are an expert in forensic linguistics and authorship verification. Analyze the writing style of the following text to determine if it was written by a single author consistently, or shows signs of multiple authors or AI-assisted writing.

TEXT:
"""
${truncated}
"""

Analyze:
1. Voice consistency (does the writing voice stay consistent throughout?)
2. Style shifts (abrupt changes in complexity, tone, formality)
3. Vocabulary patterns (consistent word choice preferences)
4. Grammatical habits (comma usage, sentence structure patterns)
5. Transition patterns (predictable vs. varied connectives)

Return ONLY valid JSON (no markdown, no extra text):
{
  "consistencyScore": <0-100>,
  "dominantStyle": "<Academic|Informal|Technical|Creative|Mixed>",
  "voiceCharacteristics": ["<characteristic1>", "<characteristic2>", "<characteristic3>"],
  "styleShifts": [
    {
      "location": "<approximately where in the text>",
      "description": "<what changed>",
      "severity": "<high|medium|low>",
      "possibleExplanation": "<why this might have happened>"
    }
  ],
  "writingPersonality": "<2-3 sentence description of the writing style and voice>",
  "authenticityVerdict": "<consistent|mixed|suspicious>",
  "authenticityReasoning": "<2-3 sentence explanation of the verdict>",
  "recommendations": ["<recommendation1>", "<recommendation2>"]
}`;

  const fallback: GeminiAuthorshipResult = {
    consistencyScore: 50,
    dominantStyle: "Unknown",
    voiceCharacteristics: [],
    styleShifts: [],
    writingPersonality: "Analysis unavailable.",
    authenticityVerdict: "mixed",
    authenticityReasoning: "Authorship analysis unavailable.",
    recommendations: [],
  };
  const raw = await callGemini(prompt);
  return safeParseJSON(raw, fallback);
}
